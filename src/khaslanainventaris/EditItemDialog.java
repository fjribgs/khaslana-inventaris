    private void loadDataToForm() {
        try {
            
            jComboBoxKategori.removeAllItems();
            
            if (dbConn == null || dbConn.conn == null) {
                JOptionPane.showMessageDialog(this, "Koneksi database tidak tersedia!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            ResultSet rsKategori = dbConn.conn.createStatement().executeQuery("SELECT * FROM categories");
            int jumlahKategori = 0;
            while (rsKategori.next()) {
                jComboBoxKategori.addItem(rsKategori.getString("name"));
                jumlahKategori++;
            }
            rsKategori.close();

            if (jumlahKategori == 0) {
                JOptionPane.showMessageDialog(this, "Peringatan: Tidak ada data kategori ditemukan di database! Silakan tambah kategori terlebih dahulu.", "Database Kosong", JOptionPane.WARNING_MESSAGE);
            }

            jComboBoxKondisi.removeAllItems();
            jComboBoxKondisi.addItem("BAIK");
            jComboBoxKondisi.addItem("KURANG BAIK");
            jComboBoxKondisi.addItem("RUSAK");

            String query = """
                SELECT i.*, c.name as category_name 
                FROM items i 
                LEFT JOIN categories c ON i.category_id = c.id 
                WHERE i.id = ?
            """;
            PreparedStatement ps = dbConn.conn.prepareStatement(query);
            ps.setInt(1, currentItemId);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                
                jTextFieldBarang.setText(rs.getString("name"));
                jTextFieldKodeBarang.setText(rs.getString("code"));
                jSpinnerJumlah.setValue(rs.getInt("jumlah"));
                jTextFieldCatatan.setText(rs.getString("notes"));
                
                if (rs.getString("category_name") != null) {
                    jComboBoxKategori.setSelectedItem(rs.getString("category_name"));
                }
                if (rs.getString("item_condition") != null) {
                    jComboBoxKondisi.setSelectedItem(rs.getString("item_condition"));
                }
            } else {
                JOptionPane.showMessageDialog(this, "Data barang dengan ID " + currentItemId + " tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
            rs.close();
            ps.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data di form: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

