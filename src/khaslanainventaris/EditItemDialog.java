    private void jButtonSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSimpanMouseClicked
        // TODO add your handling code here:
        try {

            if (jTextFieldBarang.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Nama barang tidak boleh kosong!");
                return;
            }

            if (jTextFieldKodeBarang.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Kode barang tidak boleh kosong!");
                return;
            }

            if (jComboBoxKategori.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this,
                        "Silakan pilih kategori terlebih dahulu!");
                return;
            }

            String namaKategori
                    = jComboBoxKategori.getSelectedItem().toString();

            PreparedStatement psKat
                    = dbConn.conn.prepareStatement(
                            "SELECT id FROM categories WHERE name=?");

            psKat.setString(1, namaKategori);

            ResultSet rsKat = psKat.executeQuery();

            int idKategori = 0;

            if (rsKat.next()) {
                idKategori = rsKat.getInt("id");
            }

            rsKat.close();
            psKat.close();

            String query
                    = "UPDATE items SET "
                    + "name=?, "
                    + "category_id=?, "
                    + "code=?, "
                    + "jumlah=?, "
                    + "item_condition=?, "
                    + "notes=? "
                    + "WHERE id=?";

            PreparedStatement ps
                    = dbConn.conn.prepareStatement(query);

            ps.setString(1,
                    jTextFieldBarang.getText().trim());

            ps.setInt(2,
                    idKategori);

            ps.setString(3,
                    jTextFieldKodeBarang.getText().trim());

            ps.setInt(4,
                    Integer.parseInt(
                            jSpinnerJumlah.getValue().toString()));

            ps.setString(5,
                    jComboBoxKondisi.getSelectedItem().toString());

            ps.setString(6,
                    jTextFieldCatatan.getText().trim());

            ps.setInt(7,
                    currentItemId);

            int result = ps.executeUpdate();

            ps.close();

            if (result > 0) {

                JOptionPane.showMessageDialog(
                        this,
                        "Barang berhasil diupdate!");

                mainMenu.refreshData();

                dispose();

            } else {

                JOptionPane.showMessageDialog(
                        this,
                        "Data tidak berhasil diupdate!");

            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    "Gagal update data:\n" + e.getMessage());

            e.printStackTrace();

        }
    }//GEN-LAST:event_jButtonSimpanMouseClicked

